import numpy as np
import matplotlib.pyplot as plt
from scipy import optimize

# --- Параметры фильтра (вариант 1) ---
w_p = [0, 0.60 * np.pi]      # Полоса пропускания
w_s = [0.80 * np.pi, np.pi]  # Полоса подавления
delta_p = 0.0125              # Допуск в Ωp
delta_s = 0.015               # Допуск в Ωs

def syntez_with_error(x, w_p, w_s, M):
    w_j = np.pi * (np.arange(0, M + 1) + 0.5) / (M + 1)

    K_d = np.zeros(M + 1)
    flag_p = (w_j >= w_p[0]) & (w_j <= w_p[1])
    K_d[flag_p] = 1.0
    flag_s = (w_j >= w_s[0]) & (w_j <= w_s[1])
    K_d[flag_s] = 0.0
    flag_t = (~flag_p) & (~flag_s)
    K_d[flag_t] = x

    h = np.zeros(M + 1)
    for k in range(M + 1):
        h[M - k] = (1.0 / (M + 1)) * np.sum(
            K_d * np.cos(np.pi * k * (np.arange(0, M + 1) + 0.5) / (M + 1))
        )

    w_eval = np.linspace(0, np.pi, 1024)
    A_w = h[M] + 2 * np.sum([
        h[M - k] * np.cos(w_eval * k) for k in range(1, M + 1)
    ], axis=0)

    err_p = np.abs(A_w[(w_eval >= w_p[0]) & (w_eval <= w_p[1])] - 1.0)
    err_s = np.abs(A_w[(w_eval >= w_s[0]) & (w_eval <= w_s[1])])
    max_err_p = np.max(err_p) if len(err_p) > 0 else 0
    max_err_s = np.max(err_s) if len(err_s) > 0 else 0
    E = max(max_err_p, max_err_s)

    return E, h, max_err_p, max_err_s

# --- Поиск минимального M ---
M_start = 5
M_current = M_start
found = False

while not found:
    w_j = np.pi * (np.arange(0, M_current + 1) + 0.5) / (M_current + 1)
    flag_p = (w_j >= w_p[0]) & (w_j <= w_p[1])
    flag_s = (w_j >= w_s[0]) & (w_j <= w_s[1])
    flag_t = (~flag_p) & (~flag_s)
    x0 = np.linspace(1.0, 0.0, np.sum(flag_t)) if np.sum(flag_t) > 0 else np.array([])

    result = optimize.fmin(lambda t: syntez_with_error(t, w_p, w_s, M_current)[0], x0=x0, disp=False)
    E_opt, h_opt, max_err_p, max_err_s = syntez_with_error(result, w_p, w_s, M_current)

    if max_err_p <= delta_p and max_err_s <= delta_s:
        found = True
        M_min = M_current
        h_final = h_opt
    else:
        M_current += 1

print(f"\n--- Минимальный порядок фильтра: M = {M_min}, N = {2 * M_min} ---")

