# -------------------------------

def syntez(x, w_p, w_s, M):
    """
    Возвращает ошибку E и коэффициенты h (формула (3), (4.8))
    """
    # Узлы частотной выборки: ω_j = π·(j + 0.5)/(M + 1), j = 0..M
    j = np.arange(0, M + 1)
    w_j = np.pi * (j + 0.5) / (M + 1)

    # Флаги принадлежности узлов полосам
    flag_p = (w_j >= w_p[0]) & (w_j <= w_p[1])
    flag_s = (w_j >= w_s[0]) & (w_j <= w_s[1])
    flag_t = ~(flag_p | flag_s)  # переходная зона

    # Желаемая АЧХ на узлах
    K_d = np.zeros(M + 1)
    K_d[flag_p] = 1.0
    K_d[flag_s] = 0.0
    K_d[flag_t] = x

    # Расчёт коэффициентов (формула 4.8)
    h = np.zeros(M + 1)
    for k in range(M + 1):
        cos_term = np.cos(np.pi * k * (j + 0.5) / (M + 1))
        h[M - k] = (1.0 / (M + 1)) * np.sum(K_d * cos_term)

    # Плотная сетка частот для оценки ошибки
    w_eval = np.linspace(0, np.pi, 2000)

    # АЧХ A(ω) по формуле (4.7):
    A_w = h[M] + 2 * np.sum([
        h[M - k] * np.cos(w_eval * k) for k in range(1, M + 1)
    ], axis=0)

    # Ошибка E — максимум уклонений в Ωp и Ωs (формула 3)
    err_p = np.abs(A_w[(w_eval >= w_p[0]) & (w_eval <= w_p[1])] - 1.0)
    err_s = np.abs(A_w[(w_eval >= w_s[0]) & (w_eval <= w_s[1])])
    E = np.max(np.concatenate([err_p, err_s]))

    return E, h




