# --- Функция равномерного квантования ---
def uniform_quantize(x, bits, x_min=None, x_max=None):
    """
    Равномерное (линейное) квантование с округлением до ближайшего уровня.
    - bits: число бит на отсчет (1..8)
    - возвращает массив квантованных значений той же длины, что x
    """
    L = 2 ** bits
    if x_min is None:
        x_min = np.min(x)
    if x_max is None:
        x_max = np.max(x)
    if x_max == x_min:
        return np.full_like(x, x_min)
    step = (x_max - x_min) / (L - 1)
    indices = np.round((x - x_min) / (x_max - x_min) * (L - 1)).astype(int)
    indices = np.clip(indices, 0, L - 1)
    q = x_min + indices * step
    return q

