# === 9. Равномерное квантование ===
def uniform_quantize(x, bits, x_min=None, x_max=None):
    L = 2 ** bits
    if x_min is None:
        x_min = np.min(x)
    if x_max is None:
        x_max = np.max(x)
    step = (x_max - x_min) / (L - 1)
    indices = np.round((x - x_min) / (x_max - x_min) * (L - 1)).astype(int)
    indices = np.clip(indices, 0, L - 1)
    return x_min + indices * step

